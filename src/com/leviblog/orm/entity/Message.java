package com.leviblog.orm.entity;

public class Message {
	
	private String mes_theme;
	private String mes_id;
	private String mes_content;
	private String mes_name;
	private String mes_connect;
	private String mes_datetime;
	
	public String getMes_datetime() {
		return mes_datetime;
	}
	public void setMes_datetime(String mes_datetime) {
		this.mes_datetime = mes_datetime;
	}
	public String getMes_theme() {
		return mes_theme;
	}
	public void setMes_theme(String mes_theme) {
		this.mes_theme = mes_theme;
	}
	public String getMes_id() {
		return mes_id;
	}
	public void setMes_id(String mes_id) {
		this.mes_id = mes_id;
	}
	public String getMes_content() {
		return mes_content;
	}
	public void setMes_content(String mes_content) {
		this.mes_content = mes_content;
	}
	public String getMes_name() {
		return mes_name;
	}
	public void setMes_name(String mes_name) {
		this.mes_name = mes_name;
	}
	public String getMes_connect() {
		return mes_connect;
	}
	public void setMes_connect(String mes_connect) {
		this.mes_connect = mes_connect;
	}
	
	

}
